    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
        Product product = new Product();
            product.setProductName(productNameField.getText());
            
            // Parse supplier ID (format is "ID - Name")
            String supplierStr = (String) supplierComboBox.getSelectedItem();
            int supplierId = Integer.parseInt(supplierStr.split(" - ")[0]);
            product.setSupplierId(supplierId);
           
            String categoryName = (String) categoryComboBox.getSelectedItem();
            product.setCategory(categoryName);
            
            product.setQuantityPerUnit(quantityPerUnitField.getText());
            product.setUnitPrice(Double.parseDouble(unitPriceField.getText()));
            product.setUnitsInStock(Integer.parseInt(unitsInStockField.getText()));
            product.setUnitsOnOrder(Integer.parseInt(unitsOnOrderField.getText()));
            product.setReorderLevel(Integer.parseInt(reorderLevelField.getText()));
            product.setDiscontinued(discontinuedCheckBox.isSelected());
            
            ProductService service = new ProductService();
            service.addProduct(product);
            
            JOptionPane.showMessageDialog(this, "Product added successfully!", 
                                         "Success", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers for numeric fields",
                                        "Input Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error saving product: " + ex.getMessage(),
                                        "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

