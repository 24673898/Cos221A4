private void initDialog() {
        try {
            ProductService service = new ProductService();
            
            // Load suppliers
            DefaultComboBoxModel<String> supplierModel = new DefaultComboBoxModel<>();
            for (String supplier : service.getAllSuppliers()) {
                supplierModel.addElement(supplier);
            }
            supplierComboBox.setModel(supplierModel);
            
            // Load categories
            DefaultComboBoxModel<String> categoryModel = new DefaultComboBoxModel<>();
            for (String category : service.getAllCategories()) {
                categoryModel.addElement(category);
            }
            categoryComboBox.setModel(categoryModel);
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error loading data: " + ex.getMessage(),
                                        "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }

